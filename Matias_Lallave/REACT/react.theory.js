/**
 * - ¿Qué es una librería?
 *      * Un código encapsulado (un .js) que nos proporciona una funcionalidad
 *          EJ: una serie de utilidades avanzadas de matemáticas que tendríamos que crear nosotros mismos si no.
 * 
 * - ¿Qué es React?
 *      - Es una librería que nos ayuda a crear interfaces de usuario (IU's)
 *      - Si lo juntamos con otras librerías podemos crear SPA's
 * 
 * - ¿Qué es ReactDOM?
 *      - Para crear una SPA, necesito mucho conocimiento de cómo funciona el DOM
 *      - ReactDOM -> librería que facilita la gestión del DOM
 * 
 * - ¿Qué cosas tiene ReactJS?
 *      - Componentes: La página está dividida en componentes que vamos generando y construyendo con React
 * 
 * - ¿Qué es JSX?
 *  
 *      - Es un nuevo formate de archivo que mezcla JS con HTML
 *      - Nos permite crear componentes de React de una manera más visual, sobre todo su HTML
 *          ej: const element = <h1>Hello World!</h1>;
 * 
 *      Cuando en la parte de HTML, dentro de JSX, abro {}, lo que hay dentro es un contexto de JS, 
 *      es decir, puedo acceder a variables y métodos de una clase, y lo que se escribe es JS.
 */